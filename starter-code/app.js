// require database configuration
require('./configs/db.config');

// MIDDLEWARE DE SESIÓN
require('./configs/session.config')(app)


const app_name = require('./package.json').name;
const debug = require('debug')(`${app_name}:${path.basename(__filename).split('.')[0]}`);



// Middleware Setup
app.use(logger('dev'));
app.use(bodyParser.json());
app.use(bodyParser.urlencoded({ extended: false }));
app.use(cookieParser());

// Express View engine setup

app.use(require('node-sass-middleware')({
  src:  path.join(__dirname, 'public'),
  dest: path.join(__dirname, 'public'),
  sourceMap: true
}));
      

app.set('views', path.join(__dirname, 'views'));
app.set('view engine', 'hbs');
app.use(express.static(path.join(__dirname, 'public')));
app.use(favicon(path.join(__dirname, 'public', 'images', 'favicon.ico')));



// default value for title local
app.locals.title = 'Mongoose Movies';

// MIDDLEWARE DE SESIÓN
app.use(function(req, res, next){
  res.locals.currentUser = req.session.currentUser; // {username: "Ejemplo"}
  res.locals.saludo = "Hola mudno" // String
  
  next();
});


const index = require('./routes/index');
app.use('/', index);

const index2 = require('./routes/celebrities');
app.use('/', index2);

const index3 = require('./routes/movies');
app.use('/', index3);


// Catch missing routes and forward to error handler
app.use((req, res, next) => next(createError(404)));

// Catch all error handler
app.use((error, req, res) => {
  // Set error information, with stack only available in development
  res.locals.message = error.message;
  res.locals.error = req.app.get('env') === 'development' ? error : {};

  // render the error page
  res.status(error.status || 500);
  res.render('error');
});

module.exports = app;
